import LoadingBubbles from "./loading-bubbles";
import { useState } from "react";
import Image from "next/image";

interface ImageHolderProps {
  src: string;
  width: number;
  height: number;
  alt?: string;
  hasTools?: boolean;
}

export default function ImageHolder({
  width,
  height,
  hasTools = false,
  src: imageUrl,
  alt: imageName = "Image generated by Cellesseon",
}: ImageHolderProps) {
  const [isLoading, setIsLoading] = useState<boolean>(true);
  const [isDownloading, setIsDownloading] = useState<boolean>(false);

  const handleDownload = async () => {
    setIsDownloading(true);
    try {
      const response = await fetch(
        `/api/download?url=${encodeURIComponent(imageUrl)}`,
      );
      const blob = await response.blob();
      const url = window.URL.createObjectURL(blob);
      const a = document.createElement("a");
      a.href = url;
      a.download = imageName;
      document.body.appendChild(a);
      a.click();
      document.body.removeChild(a);
      window.URL.revokeObjectURL(url);
    } catch (error) {
      console.error("Download failed:", error);
    }
    setIsDownloading(false);
  };

  return (
    <div className="ImageHolder relative mb-2 mt-3 flex max-h-max max-w-max items-center justify-center overflow-hidden rounded bg-jwdAqua-500/10 shadow-sm transition-all">
      {isLoading ? (
        <span className="flex p-4">
          <LoadingBubbles size="small" />
        </span>
      ) : (
        hasTools && (
          <button
            type="button"
            className="icon-btn absolute bottom-2 right-2 z-10 bg-black/50 text-white hover:bg-black/60"
            onClick={handleDownload}
            disabled={isDownloading}
            aria-label="Download generated image"
          >
            {isDownloading ? (
              <LoadingBubbles size="small" className="!w-auto gap-0.5" />
            ) : (
              <i className="bi bi-download"></i>
            )}
          </button>
        )
      )}

      <Image
        priority
        src={imageUrl}
        width={width}
        height={height}
        loading="eager"
        onLoad={() => setIsLoading(false)}
        alt={imageName}
        className={`${isLoading ? "hidden" : "animate-fade animate-once animate-duration-500 animate-delay-300 animate-ease-in-out"}`}
        sizes={`(max-width: 768px) 100vw, (max-width: 1200px) 50vw, ${width}px`}
      />
    </div>
  );
}
